// ============ Payment Status Mapping ============

import { GenericPaymentTxnStatus } from '../Payment';

export const mapRazorpayStatusToGeneric = (status: string): GenericPaymentTxnStatus => {
    switch (status) {
        case RazorpayPaymentStatus.Captured:
            return GenericPaymentTxnStatus.Success;
        case RazorpayPaymentStatus.Failed:
            return GenericPaymentTxnStatus.Failed;
        case RazorpayPaymentStatus.Refunded:
            return GenericPaymentTxnStatus.Refunded;
        case RazorpayPaymentStatus.Created:
        case RazorpayPaymentStatus.Authorized:
        default:
            return GenericPaymentTxnStatus.Pending;
    }
};

export const mapRazorpayOrderStatusToGeneric = (status: string): GenericPaymentTxnStatus => {
    switch (status) {
        case RazorpayOrderStatus.Paid:
            return GenericPaymentTxnStatus.Success;
        case RazorpayOrderStatus.Created:
        case RazorpayOrderStatus.Attempted:
        default:
            return GenericPaymentTxnStatus.Pending;
    }
};

