// ============ Create UPI Payment ============
// Reference: https://razorpay.com/docs/payments/payment-gateway/s2s-integration/payment-methods/upi/

export interface RazorpayCreateUPIPaymentRequest {
    /** Amount in smallest currency unit (paise) */
    amount: number;
    /** Currency code (INR) */
    currency: string;
    /** Razorpay Order ID */
    order_id: string;
    /** Customer email */
    email?: string;
    /** Customer phone number */
    contact?: string;
    /** Payment method - "upi" */
    method: 'upi';
    /** Customer ID (required for saving VPA) */
    customer_id?: string;
    /** Whether to save VPA as token */
    save?: boolean;
    /** Client's IP address */
    ip?: string;
    /** Referer header value */
    referer?: string;
    /** User agent string */
    user_agent?: string;
    /** Payment description */
    description?: string;
    /** Additional notes */
    notes?: RazorpayNotes;
    /** UPI specific options */
    upi: {
        /** UPI flow type: intent or collect */
        flow: RazorpayUPIFlow | string;
        /** VPA/UPI ID (required for collect flow) */
        vpa?: string;
        /** Collect request expiry time in minutes (default: 5, max: 5760) */
        expiry_time?: number;
    };
    /** Callback URL for payment status */
    callback_url?: string;
    /** Fee amount in paise (for Customer Fee Bearer - CFB) */
    fee?: number;
}

